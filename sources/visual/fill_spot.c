#include <filler.h>

void	fill_spot(t_vis *v, int x, int y, char c)
{
	int i;
	int j;

	i = 1;
	x *= v->zoom;
	y *= v->zoom;
	while (++i < v->zoom - 1)
	{
		j = 1;
		while (++j < v->zoom - 1)
		{
			putpixel(v, x + j, y + i, c);
		}
	}
}
